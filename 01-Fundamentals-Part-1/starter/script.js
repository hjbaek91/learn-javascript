// #1 Values and Variables

// There are many different kinds of 'values' in JavaScript.
// The most common values are 'strings' and 'numbers'.

console.log(40 + 8 + 23 - 10);
console.log("John");
console.log(23);

// We can use 'variables' to store values.
// Variables are like boxes with labels that can be used to find the values whenever convenient.
// By just changing the values, we can change the content within the variables.

let firstName = "Steve";

console.log(firstName);
console.log(firstName);
console.log(firstName);

// There are naming conventions for JavaScript variables.
// It is encouraged to use 'camel-casing' when naming variables with more than one word.
// Variable names should be descriptive.

let myCurrentJob = "Programmer";
let myPreviousJob = "Biochemist";

let job1 = "programmer";
let job2 = "biochemist";

console.log(myCurrentJob);
